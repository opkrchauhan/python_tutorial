# Assignment operators
a = 10
b = 5

print("Initial value of a:", a)
print("Initial value of b:", b)

a += b
print("After a += b, value of a:", a)

a -= b
print("After a -= b, value of a:", a)

a *= b
print("After a *= b, value of a:", a)

a /= b
print("After a /= b, value of a:", a)

a //= b
print("After a //= b, value of a:", a)

a %= b
print("After a %= b, value of a:", a)

a **= b
print("After a **= b, value of a:", a)